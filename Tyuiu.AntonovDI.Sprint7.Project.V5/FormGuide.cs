using System;
using System.Windows.Forms;

namespace Tyuiu.AntonovDI.Sprint7.Project.V5
{
    public partial class FormGuide : Form
    {
        public FormGuide()
        {
            InitializeComponent();
            LoadGuideText();
        }

        private void LoadGuideText()
        {
            richTextBoxGuide.Text =
@"ОБЩАЯ ИНФОРМАЦИЯ
--------------------------------------------------
Программа «Оптовая база» предназначена для ведения
учёта товаров и продавцов.

В программе используются CSV-файлы в качестве базы данных.


ТАБЛИЦЫ
--------------------------------------------------
В программе доступны две таблицы:

1) Товары
   - ID товара
   - Наименование
   - Страна производитель
   - Количество на складе
   - Цена
   - ID продавца

2) Продавцы
   - ID продавца
   - ФИО
   - Телефон


ДОБАВЛЕНИЕ ЗАПИСЕЙ
--------------------------------------------------
Меню → Добавить

В открывшейся форме необходимо заполнить все поля.
ID генерируется автоматически.


РЕДАКТИРОВАНИЕ
--------------------------------------------------
Меню → Изменить

Перед редактированием необходимо выбрать запись
в таблице.


УДАЛЕНИЕ
--------------------------------------------------
Меню → Удалить

Выбранная запись будет удалена после подтверждения.


ПОИСК
--------------------------------------------------
Поле поиска позволяет фильтровать записи:
- для товаров — по наименованию
- для продавцов — по ФИО


ФИЛЬТР
--------------------------------------------------
Меню → Фильтр

Позволяет применять расширенные условия:
- диапазоны ID
- цена и количество (от/до)
- страны производители
- продавцы

Фильтр сохраняется до сброса.


СТАТИСТИКА
--------------------------------------------------
Меню → Статистика

Отображает аналитическую информацию
по выбранной таблице.


ЭКСПОРТ CSV
--------------------------------------------------
Меню → Экспорт CSV

Позволяет сохранить текущую таблицу
в отдельный CSV-файл.


ДИАГРАММЫ
--------------------------------------------------
Доступны только для таблицы товаров.


СОХРАНЕНИЕ ДАННЫХ
--------------------------------------------------
Все изменения автоматически сохраняются
в CSV-файлы.";
        }

        private void ButtonClose_Click(object sender, EventArgs e)
        {
            Close();
        }
    }
}
